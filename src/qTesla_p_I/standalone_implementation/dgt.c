void dgt(poly x)
{
  int m, distance;
  int j1, j2, j, k;
  int32_t a, temp_re, temp_img;
  
  distance = 512;
  for(m = 1; m < 512; m <<= 1)
  {
    for(k = 0; k < m; ++k)
    {
      j1 = 2 * k * distance;
      j2 = j1 + distance - 1;

      a = gj[k];
      for(j = j1; j <= j2; j = j+2)
      {
        temp_re = reduce((int64_t)a * x[j + distance]);
        temp_img = reduce((int64_t)a * x[j + distance + 1]);

        x[j + distance] = barr_reduce64(x[j] + (2*PARAM_Q - temp_re));
        x[j + distance + 1] = barr_reduce64(x[j + 1] + (2*PARAM_Q - temp_img));
        
        x[j] = barr_reduce64(x[j] + temp_re);
        x[j + 1] = barr_reduce64(x[j + 1] + temp_img);
      }
    }
    distance >>= 1;
  }
}

void idgt(poly poly) 
{
  int distance, j1, jtwiddle, m, j, h;
  int32_t sub_re, sub_img;

  //h = 0;
  distance = 2;
  for(m = 512; m > 1; m >>= 1)
  {
    for(j1 = 0; j1 < distance; j1 = j1 + 2)
    {
      //jtwiddle = h;
      jtwiddle = 0;
      for(j = j1; j < PARAM_N; j = j + 2*distance)
      {
        sub_re = barr_reduce64(poly[j] + (2*PARAM_Q - poly[j+distance]));
        sub_img = barr_reduce64(poly[j+1] + (2*PARAM_Q - poly[j+distance+1]));
        
        poly[j] = barr_reduce64(poly[j] + poly[j+distance]);
        poly[j+1] = barr_reduce64(poly[j+1] + poly[j+distance+1]);

        poly[j+distance] = reduce((int64_t)invgj[jtwiddle] * sub_re);
        poly[j+distance+1] = reduce((int64_t)invgj[jtwiddle] * sub_img);

        jtwiddle += 1;
      }
    }
    //h += m;
    distance <<= 1;
  }

  for(j = 0; j < PARAM_N; ++j)
    poly[j] = (int32_t)barr_reduce64(reduce((int64_t)poly[j] * 8388608)); // 512^-1 * 2^32 mod p

}